export function traducir(especie) {
  switch (especie) {
    case "dog":
      return "Perro";
    case "cat":
      return "Gato";
    case "bird":
      return "Ave";
    case "barnyard":
      return "Animal de corral";
    case "reptile":
      return "Reptil";
    case "smallfurry":
      return "Roedor";
    case "horse":
      return "Caballo";
    case "pig":
      return "Cerdo";
  }
  
}
